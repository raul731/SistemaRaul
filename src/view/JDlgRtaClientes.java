        rtaCliente.setRtaRg(jFmtRg.getText());
        rtaCliente.setRtaPais(jCboPais.getSelectedIndex());
        rtaCliente.setRtaRua(jTxtRua.getText());
        rtaCliente.setRtaNumeroRua(jTxtNumRua.getText());
